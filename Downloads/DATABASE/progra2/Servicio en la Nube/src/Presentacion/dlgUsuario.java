      private void cargarDatos() {

            while (modelo.getRowCount() > 0) {
                  modelo.removeRow(0);
            }

            ArrayList<clsUsuario> lista;
            clsUsuarioAD userAD = new clsUsuarioAD();

            try {
                  lista = userAD.listado();
                  for (clsUsuario user : lista) {
                        String fila[] = {user.getIdUsuario() + "", user.getNombre(), user.getEmail(), user.getContraseña(), user.getTelefono()};
                        modelo.addRow(fila);
                  }
            } catch (Exception e) {
                  JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }

            btnGuardar.setEnabled(true);
            btnModificar.setEnabled(false);
            btnNuevoUsuario.setEnabled(true);

      }

