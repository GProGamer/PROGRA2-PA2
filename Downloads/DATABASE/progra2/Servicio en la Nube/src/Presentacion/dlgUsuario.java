    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
          // TODO add your handling code here:
          String email = this.txtEmail.getText();
          String nombre = this.txtNombre.getText();
          String contraseña = this.txtContraseña.getText();

          String telefono = this.txtTelefono.getText();
          clsUsuario user = new clsUsuario(email, nombre, contraseña, telefono);

          clsUsuarioAD conexion = new clsUsuarioAD();
          conexion.agregar(user);
          cargarDatos();
    }//GEN-LAST:event_btnGuardarActionPerformed

      private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
            // TODO add your handling code here:
            int userId = Integer.parseInt(this.txtID.getText());
            String email = this.txtEmail.getText();
            String nombre = this.txtNombre.getText();
            String contraseña = this.txtContraseña.getText();

            String telefono = this.txtTelefono.getText();
            clsUsuario user = new clsUsuario(userId, email, nombre, contraseña, telefono);

            clsUsuarioAD conexion = new clsUsuarioAD();
            conexion.modificar(user);
            cargarDatos();
      }//GEN-LAST:event_btnModificarActionPerformed

      private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
            // TODO add your handling code here:
            int filaSel = tblDatos.getSelectedRow();

            txtID.setText(modelo.getValueAt(filaSel, 0).toString());

            txtNombre.setText(modelo.getValueAt(filaSel, 1).toString());
            txtEmail.setText(modelo.getValueAt(filaSel, 2).toString());
            txtContraseña.setText(modelo.getValueAt(filaSel, 3).toString());
            txtTelefono.setText(modelo.getValueAt(filaSel, 4).toString());

            btnGuardar.setEnabled(false);
            btnModificar.setEnabled(true);
            btnNuevoUsuario.setEnabled(true);
      }//GEN-LAST:event_tblDatosMouseClicked

