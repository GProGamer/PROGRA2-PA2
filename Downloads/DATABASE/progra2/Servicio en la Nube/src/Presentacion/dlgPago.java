    private void cargarDatos(int user_id) {

        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }

        //ArrayList<clsDetallePago> lista;
        clsDetallePagoAD detallePagoAD = new clsDetallePagoAD();
        ResultSet lista;

        try {
            lista = detallePagoAD.listado(user_id);
            while (lista.next()) {
                String fila[] = {lista.getString("idPago") + "", lista.getString("fecha"), lista.getString("usuario.nombre"),
                    lista.getString("servicios_nube.nombre"), lista.getDouble("servicios_nube.precio") + "",
                    lista.getInt("alquiler_de_servicio.tiempoServicio") + "",
                    lista.getDouble("detalles_de_pago.montoPagar") + "", lista.getString("detalles_de_pago.Pagado")};
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }

    }

