    private int getUserIdByEmail() {
        String email = txtEmail.getText();
        clsUsuarioAD usuario = new clsUsuarioAD();
        return usuario.getIdByEmail(email);
    }

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int userId = getUserIdByEmail();
        cargarDatos(userId);

        limpiar();
        
        btnPagarTodo.setEnabled(true);
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        // TODO add your handling code here:
        int pagoId = Integer.parseInt(txtID.getText());
        clsDetallePagoAD detallePagoAD = new clsDetallePagoAD();
        detallePagoAD.modificar(pagoId);
        int userId = getUserIdByEmail();
        cargarDatos(userId);
    }//GEN-LAST:event_btnPagarActionPerformed

      private void btnPagarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarTodoActionPerformed
          // TODO add your handling code here:
          clsDetallePagoAD detallePagoAD = new clsDetallePagoAD();
          int userId = getUserIdByEmail();
          detallePagoAD.modificarTodo(userId);
      }//GEN-LAST:event_btnPagarTodoActionPerformed

    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        // TODO add your handling code here:
        int filaSel = tblDatos.getSelectedRow();

        txtID.setText(modelo.getValueAt(filaSel, 0).toString());
        txtFecha.setText(modelo.getValueAt(filaSel, 1).toString());
        txtMonto.setText(modelo.getValueAt(filaSel, 6).toString());

        String pagado = modelo.getValueAt(filaSel, 7).toString();

        if (pagado.equals("SI")) {
            btnPagar.setEnabled(false);
        }

    }//GEN-LAST:event_tblDatosMouseClicked

