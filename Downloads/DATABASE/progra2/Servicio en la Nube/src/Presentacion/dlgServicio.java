      private void cargarDatos() {

            while (modelo.getRowCount() > 0) {
                  modelo.removeRow(0);
            }

            ArrayList<clsServicioNube> lista;
            clsServicioNubeAD servicioAD = new clsServicioNubeAD();

            try {
                  lista = servicioAD.listado();
                  for (clsServicioNube servicio : lista) {
                        String fila[] = {servicio.getIdServicio() + "", servicio.getNombre(), servicio.getPrecio() + ""};
                        modelo.addRow(fila);
                  }
            } catch (Exception e) {
                  JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }

            btnGuardar.setEnabled(true);
            btnModificar.setEnabled(false);
            btnNuevo.setEnabled(true);

      }

