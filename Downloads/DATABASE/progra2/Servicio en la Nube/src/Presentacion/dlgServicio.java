      private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
            // TODO add your handling code here:
            double precio = Double.parseDouble(this.txtPrecio.getText());
            String nombre = this.txtTipoServicio.getText();
            int servicioId = Integer.parseInt(this.txtID.getText());

            clsServicioNube servicio = new clsServicioNube(servicioId, nombre, precio);
            clsServicioNubeAD conexion = new clsServicioNubeAD();

            conexion.modificar(servicio);
            cargarDatos();
      }//GEN-LAST:event_btnModificarActionPerformed

      private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
            // TODO add your handling code here:
            int filaSel = tblDatos.getSelectedRow();

            txtID.setText(modelo.getValueAt(filaSel, 0).toString());
            txtTipoServicio.setText(modelo.getValueAt(filaSel, 1).toString());
            txtPrecio.setText(modelo.getValueAt(filaSel, 2).toString());

            btnGuardar.setEnabled(false);
            btnModificar.setEnabled(true);
            btnNuevo.setEnabled(true);
      }//GEN-LAST:event_tblDatosMouseClicked

