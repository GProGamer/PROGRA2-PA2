    private void mostrarServicio() {

        clsServicioNubeAD servicios = new clsServicioNubeAD();
        for (String nombreServicio : servicios.mostrarServicio()) {
            cboTipoServicio.addItem(nombreServicio);
        }

    }

    private void limpiar() {
        txtDuracion.setText("");
        txtFechaFinal.setText("");
        txtFechaInicio.setText("");
        txtID.setText("");

        btnAlquilar.setEnabled(true);
        btnModificar.setEnabled(false);
    }

    private String getNombreServicioByServicioId(int servicioId) {
        clsServicioNubeAD serviciosAD = new clsServicioNubeAD();
        return serviciosAD.getNombreServicioByServicioId(servicioId);
    }

    private void cargarDatos(int user_id) {

        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }

        ArrayList<clsAlquilerServicio> lista;
        clsAlquilerServicioAD servicioAD = new clsAlquilerServicioAD();

        try {
            lista = servicioAD.listado(user_id);
            for (clsAlquilerServicio servicio : lista) {
                String fila[] = {servicio.getIdAlquiler() + "", getNombreServicioByServicioId(servicio.getServicio_id()), servicio.getTiempoDeServicio() + "", servicio.getFechaInicio(), servicio.getFechaFinal()};
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }

    }

