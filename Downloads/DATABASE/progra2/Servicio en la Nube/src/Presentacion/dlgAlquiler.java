    private int getUserIdByEmail() {
        String email = txtEmail.getText();
        clsUsuarioAD usuario = new clsUsuarioAD();
        return usuario.getIdByEmail(email);
    }

    private int getServicioIdByName() {
        String servicio = cboTipoServicio.getSelectedItem().toString();
        clsServicioNubeAD servicioAD = new clsServicioNubeAD();
        return servicioAD.getServicioIdByNombre(servicio);
    }

