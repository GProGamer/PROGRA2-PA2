    private void btnAlquilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlquilarActionPerformed
        // TODO add your handling code here:
        int user_id = getUserIdByEmail();

        int duracion = Integer.parseInt(txtDuracion.getText());
        String fechaInicial = txtFechaInicio.getText();

        int servicioId = getServicioIdByName();

        clsAlquilerServicio alquilerServicio = new clsAlquilerServicio(duracion, fechaInicial, servicioId, user_id);
        clsAlquilerServicioAD conexion = new clsAlquilerServicioAD();
        conexion.agregar(alquilerServicio);

        cargarDatos(user_id);
    }//GEN-LAST:event_btnAlquilarActionPerformed

      private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
          // TODO add your handling code here:
          int user_id = getUserIdByEmail();
          int idAlquiler = Integer.parseInt(txtID.getText());
          int duracion = Integer.parseInt(txtDuracion.getText());
          String fechaInicial = txtFechaInicio.getText();

          int servicioId = getServicioIdByName();

          clsAlquilerServicio alquilerServicio = new clsAlquilerServicio(idAlquiler, duracion, fechaInicial, servicioId, user_id);
          clsAlquilerServicioAD conexion = new clsAlquilerServicioAD();
          conexion.modificar(alquilerServicio);

          cargarDatos(user_id);

      }//GEN-LAST:event_btnModificarActionPerformed

      private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
          // TODO add your handling code here:
          int filaSel = tblDatos.getSelectedRow();

          txtID.setText(modelo.getValueAt(filaSel, 0).toString());
          cboTipoServicio.setSelectedItem(modelo.getValueAt(filaSel, 1).toString());
          txtDuracion.setText(modelo.getValueAt(filaSel, 2).toString());

          txtFechaInicio.setText(modelo.getValueAt(filaSel, 3).toString());
          txtFechaFinal.setText(modelo.getValueAt(filaSel, 4).toString());

          btnAlquilar.setEnabled(false);
          btnModificar.setEnabled(true);

      }//GEN-LAST:event_tblDatosMouseClicked

